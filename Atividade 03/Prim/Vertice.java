
public class Vertice {

	private int peso;
	private String vertice;
	
	public Vertice(int peso, String vertice) {
		this.peso = peso;
		this.vertice = vertice;
	}
	
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	public String getVertice() {
		return vertice;
	}
	public void setVertice(String vertice) {
		this.vertice = vertice;
	}
	
	
	
}
